    public void validar() {
        String user = txtUser.getText();
        String contra = txtPass.getText();
        String cargo = ComboBoxCargo.getSelectedItem().toString();
        if (txtUser.getText().equals("") || txtPass.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Ingrese los datos pedidos en las cajas de texto");
        } else {
            empleado = dao.empleadoGet(cargo, user, contra);
            if (empleado != null) {
                String codigoEmps = dao.obtenerCodPorCredenciales(user, contra);
                dao.llevarCodAuxiliar(codigoEmps);
                PanelPrincipal.cargoActual = cargo;
                PanelPrincipal prin = new PanelPrincipal();
                prin.setVisible(true);
                prin.setLocationRelativeTo(null);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Verifique sus datos o puede que su credencial esté inactivo");
            }
        }
    }
    
